function CREATE_POSITIONCONTROLLER(t)
	local a_PC = X.AnimationControllerInfo:new()
	akeys = t.akeys
	a_PC:CreateControllerVector3()
	for i, v in ipairs(akeys) do
		a_PC:SetValuesAtKey(i,v[2][1],v[2][2],v[2][3])	
	end	
	a_PC:SetTarget(t.target)
	a_PC:Register()		
end